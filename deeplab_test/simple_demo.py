class DeepLabModel(object):
    """Class to load deeplab model and run inference."""
    INPUT_TENSOR_NAME = 'ImageTensor:0'
    OUTPUT_TENSOR_NAME = 'SemanticPredictions:0'
    INPUT_SIZE = 513
    FROZEN_GRAPH_NAME = 'frozen_inference_graph'

    def __init__(self, tarball_path):
        """Creates and loads pretrained deeplab model."""
        self.graph = tf.Graph()

        graph_def = None
        # Extract frozen graph from tar archive.
        tar_file = tarfile.open(tarball_path)
        for tar_info in tar_file.getmembers():
            if self.FROZEN_GRAPH_NAME in os.path.basename(tar_info.name):   #os.path.basename(),返回path最后的文件名。若path以/或\结尾，返回空
                file_handle = tar_file.extractfile(tar_info)                #file_handle 勾住的是一个点bp文件，tf的模型。
                graph_def = tf.GraphDef.FromString(file_handle.read())      #但是file_handle还在压缩包里，还要读出来才是真正的点bp文件
                break

        tar_file.close()

        if graph_def is None:
            raise RuntimeError('Cannot find inference graph in tar archive.')

        with self.graph.as_default():
            tf.import_graph_def(graph_def, name='')

        self.sess = tf.Session(graph=self.graph)

    def run(self, image):
        """Runs inference on a single image.

        Args:
        image: A PIL.Image object, raw input image.

        Returns:
        resized_image: RGB image resized from original input image.
        seg_map: Segmentation map of `resized_image`.
        """
        width, height = image.size
        resize_ratio = 1.0 * self.INPUT_SIZE / max(width, height)
        target_size = (int(resize_ratio * width), int(resize_ratio * height))
        resized_image = image.convert('RGB').resize(target_size, Image.ANTIALIAS)   #PIL库，resize（）第二个参数表示图像品质对应不同的重新采样方法
        batch_seg_map = self.sess.run(
            self.OUTPUT_TENSOR_NAME,
            feed_dict={self.INPUT_TENSOR_NAME: [np.asarray(resized_image)]})    #asarray将列表[]转化为数组([])
                        # numpy.asarray(a,'f')  #将图片以数组的形式传入graph，转换的时候可以设置类型
                        # array([ 1.,  2.], dtype=float32) 
        seg_map = batch_seg_map[0]  #batch_seg_map比seg_map多了一维

        # print('image dtype :',image.dtype)  #'JpegImageFile' object has no attribute 'dtype'
        # print('resized_image dtype:',resized_image[0].dtype)#'Image' object does not support indexing
        # print('resized_image dtype:',resized_image.dtype)#''Image' object has no attribute 'dtype'
        # print('resized_image dtype:',resized_image.dtype)#''Image' object has no attribute 'dtype'
        print('batch_seg_map dtype:',batch_seg_map[0].dtype)#int64
        # print('seg_map dtype:',seg_map[0].dtype)#int64
        return resized_image, seg_map

def create_pascal_label_colormap():
    """Creates a label colormap used in PASCAL VOC segmentation benchmark.

    Returns:
        A Colormap for visualizing segmentation results.
    """
    colormap = np.zeros((256, 3), dtype=int)
    ind = np.arange(256, dtype=int)

    for shift in reversed(range(8)):
        for channel in range(3):
            colormap[:, channel] |= ((ind >> channel) & 1) << shift
        ind >>= 3

    return colormap

def label_to_color_image(label):
    """Adds color defined by the dataset colormap to the label.

    Args:
        label: A 2D array with integer type, storing the segmentation label.

    Returns:
        result: A 2D array with floating type. The element of the array
        is the color indexed by the corresponding element in the input label
        to the PASCAL color map.

    Raises:
        ValueError: If label is not of rank 2 or its value is larger than color
        map maximum entry.
    """
    if label.ndim != 2:
        raise ValueError('Expect 2-D input label')

    colormap = create_pascal_label_colormap()

    if np.max(label) >= len(colormap):
        raise ValueError('label value too large.')

    return colormap[label]

